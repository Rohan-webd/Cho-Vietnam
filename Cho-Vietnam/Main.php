<h1>FOOD</h1>

<p> A sharing style menu, each meal is prepared using time-tested recipes that translate to wholesome and satiating dishes and are inclusive of dietary restrictions with gluten-free and vegan options, courtesy Chef Vaibhav. <br><br> A force to be reckoned with, Chef Vaibhav combined his rich experience in the culinary landscape with meticulously researched ingredients to build each of the recipes borrowed from across the regions of Vietnam. From Banh Mi, Banh Xeo, Chao Bo, Bun Cha, Thom Kem, Suon Nuong to the ubiquitous Pho, the menu features treasure troves that take you on an epicurean journey.<br><br> Chef Vaibhav’s travels have ensured they deliver authentic Vietnamese flavours infused with contemporary techniques to the palates of the city’s discerning audience. </p>
