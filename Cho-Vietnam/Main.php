<p>
  <span id="lg-screen">


  <span>CHO </span>invites you to experience the spirit of Vietnam in a fresh, real and homely Vietnamese all day kitchen and bar.  At the city’s au-courant Ambawatta complex in Mehrauli, CHÔ, derivative of the word ‘CHÔ nhà’, which means a cottage or a home in Vietnamese, reimagines the vibrant street food and regional favourites of the Mekong Delta and Nguyen Dynasty, housing a symphony of delightful textures and a healthy mix of traditional and avant-garde dishes. <br><br> 

CHÔ, an all-day restaurant and bar is the quintessential destination for a casual evening with friends or for shindigs. Nestled in a 160 year old haveli overlooking the city’s revered Qutub Minar, nature is the protagonist of the space, where indoors seamlessly flow into lush tropical outdoors. Punctuated with windows, wicker, bamboo and steel that are popular materials in the architecture of a Vietnamese household, CHÔ explores innovative ways to bring nature closer to you. An urban escape rooted amidst beautiful tall trees framed through broad arches, the casual dining room allows you to reset and restore without having to fly out to the far East. Adorned with vibrant wall art, handmade floor tiles and handcrafted wicker baskets, the space echoes the hospitable and tropical essence of a family cottage in Vietnam, making it a relaxed home away from home.

CHÔ is the first project of Santosh Food and Beverages and ABV Hospitality.<br><br>


<b>About Santosh Group: </b> <br><br>
Hailing from the rural backdrops of Tamil Nadu, Dr. P Mahalingam keenly observed the inadequate medical services as he had a dream to provide medical care to the underprivileged population of this country.

The first step taken to realize his dream was the establishment of Santosh Hospitals at Chennai in 1990. As a true visionary, he was never complacent on applauds and laurels he earned, rather was driven by a zeal to do more by establishing Medical College, Dental College & Paramedical Colleges in Ghaziabad (NCR Delhi). <br><br>

Dr P. Mahalingam along with his son Dr Santosh Mahalingam are entering into the hospitality industry and their newest venture is Cho – Vietnamese kitchen and bar. <br><br>

The group owns, manages multiple Hospitals & Educational Institutes
